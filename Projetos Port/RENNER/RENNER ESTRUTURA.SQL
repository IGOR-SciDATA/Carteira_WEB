CREATE TABLE RNR_CLIENTES
(IDCLIENTE INTEGER NOT NULL,
NOMECLIENTE VARCHAR2(30),
CPFCLIENTE VARCHAR2(15),
NUMEROCLIENTE VARCHAR2(15),
CEPCLIENTE VARCHAR2(9),
RUACLIENTE VARCHAR2(40),
NUMCASA VARCHAR2(6),
CIDADECLIENTE VARCHAR2(15),
ESTADOCLIENTE VARCHAR2(2) CHECK (ESTADOCLIENTE IN('AC','AL','AP','AM','BA','CE','DF','ES','GO','MA','MT','MS','MG','PA',
'PB','PR','PE','PI','RJ','RN','RS','RO','RR','SC','SP','SE','TO')),
PRIMARY KEY(IDCLIENTE));

CREATE TABLE RNR_ENTREGA
(CODENTREGA VARCHAR2(5) NOT NULL,
IDCLIENTE INTEGER,
CODPROD INTEGER,
CODDEP INTEGER,
DTENTREGA DATE,
PRIMARY KEY (CODENTREGA)
);

CREATE TABLE RNR_PRODUTOS
(CODPROD INTEGER NOT NULL,
TIPOPROD VARCHAR2(9) CHECK (TIPOPROD IN ('CAMISETA','SHORTS','CALÇA','TENIS')),
MODELO VARCHAR2(25),
COR VARCHAR2(15),
PRIMARY KEY (CODPROD)
);

CREATE TABLE RNR_DEP
(CODDEP INTEGER NOT NULL,
CEPDEP VARCHAR2(9),
RUADEP VARCHAR2(40),
TIPOPROD VARCHAR2(9),
QNTPROD NUMBER(4),
PRIMARY KEY (CODDEP)
);

CREATE TABLE RNR_LOJA
(CODLOJA INTEGER NOT NULL,
CEPLOJA VARCHAR2(9),
ENDLOJA VARCHAR2(40),
PRIMARY KEY (CODLOJA)
);

CREATE TABLE RNR_VENDEDOR
(CODVENDEDOR VARCHAR2(3) NOT NULL,
CODLOJA INTEGER,
PRIMARY KEY(CODVENDEDOR)
);

CREATE TABLE RNR_VENDAS
(CODVENDA INTEGER NOT NULL,
CODLOJA INTEGER,
CODVENDEDOR VARCHAR2(3) REFERENCES RNR_VENDEDOR(CODVENDEDOR),
IDCLIENTE INTEGER,
CODPROD INTEGER,
QNTPROD NUMBER(4),
VALTOTAL NUMBER(6),
CODENTREGA VARCHAR2(5) REFERENCES RNR_ENTREGA(CODENTREGA),
VALFRETE NUMBER(6),
DTVENDA DATE,
PRIMARY KEY (CODVENDA)
);